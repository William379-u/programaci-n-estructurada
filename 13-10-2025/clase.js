const cuadrado = (numero) => numero * numero;
console.log("El cuadrado de 4 es:", cuadrado(4));

const promedio = (num1, num2, num3) => (num1 + num2 + num3) / 3;
console.log("El promedio de 4, 6 y 8 es:", promedio(4, 6, 8));

const areaTriangulo = (base, altura) => (base * altura) / 2;
console.log("El área del triángulo con base 10 y altura 5 es:", areaTriangulo(10, 5));
